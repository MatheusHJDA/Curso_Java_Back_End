    private void tableClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableClientesMouseClicked
        int selecionarExcluir = tableClientes.getSelectedRow();
        Long cpf = (Long) tableClientes.getValueAt(selecionarExcluir, 1);
        
        Cliente cliente = this.clienteDAO.consultar(cpf);
        
        textNome.setText(cliente.getNome());
        textCpf.setText(cliente.getCpf().toString());
        textEndereco.setText(cliente.getEndereco());
        textNumero.setText(cliente.getNumero());
    }//GEN-LAST:event_tableClientesMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int selecionarExcluir = tableClientes.getSelectedRow();
        
        if (selecionarExcluir >= 0){
            int result = JOptionPane.showConfirmDialog(this,"Deseja realmente excluir ?", "Cuidado", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(result == JOptionPane.YES_OPTION){
                Long cpf = (Long) tableClientes.getValueAt(selecionarExcluir, 1);
                this.clienteDAO.excluir(cpf);
                modelo.removeRow(selecionarExcluir);
                
                JOptionPane.showMessageDialog(null,"Cliente excluido com sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null,"Cliente não selecionado ou não encontrado", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            }
        }               
    }//GEN-LAST:event_btnExcluirActionPerformed

