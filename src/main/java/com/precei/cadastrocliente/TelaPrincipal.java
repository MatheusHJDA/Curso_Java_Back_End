    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome = textNome.getText();
        String cpf = textCpf.getText();
        String numero = textNumero.getText();
        String endereco = textEndereco.getText();
        if(!isCamposValidos(nome, cpf, numero, endereco)){
            JOptionPane.showMessageDialog(null,"Digite todos os campos ", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Cliente cliente = new Cliente(nome, cpf, numero, endereco);
        Boolean isCadastrado = this.clienteDAO.cadastrar(cliente);
        
        if(isCadastrado){
            modelo.addRow(new Object[]{cliente.getNome(), cliente.getCpf(), cliente.getNumero(), cliente.getEndereco()});
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(null,"Cliente já cadastrado", "Atenção", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

